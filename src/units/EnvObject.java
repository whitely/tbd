package units;

public class EnvObject extends Unit{
	protected int x;
	protected int y;
	protected int r;
	protected String graphics;
	
	public int getx()
	{
		return x;
	}
	
	public void setx(int val)
	{
		x = val;
	}
	
	public int gety()
	{
		return y;
	}
	
	public void sety(int val)
	{
		y = val;
	}
	
	public int getr()
	{
		return r;
	}
	
	public void setr(int val)
	{
		r = val;
	}
	
	public String getGraphics()
	{
		return graphics;
	}
	
	public void setGraphics(String val)
	{
		graphics = val;
	}
}
