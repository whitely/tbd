package world;

public class RectTerrain extends Terrain {
	protected int width;
	protected int height;
	
	public int getWidth()
	{
		return width;
	}
	
	public void setWidth(int w)
	{
		width = w;
	}
	
	public int getHeight()
	{
		return height;
	}
	
	public void setHeight(int h)
	{
		height = h;
	}
}
