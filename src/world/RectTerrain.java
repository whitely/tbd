package world;

public class RectTerrain extends Terrain {
	protected int x1;
	protected int y1;
	protected int x2;
	protected int y2;
	
	public int getx1()
	{
		return x1;
	}
	
	public void setx1(int val)
	{
		x1 = val;
	}
	
	public int gety1()
	{
		return y1;
	}
	
	public void sety1(int val)
	{
		y1 = val;
	}
	
	public int getx2()
	{
		return x2;
	}
	
	public void setx2(int val)
	{
		x2 = val;
	}
	
	public int gety2()
	{
		return y2;
	}
	
	public void sety2(int val)
	{
		y2 = val;
	}
}
